# function to sum up two numbers and return the result
def sum(a, b):
    return a + b
# function to subtract two numbers and return the result
def subtract(a, b):
    return a - b
# function to calcualte square root of a number and return the result
def square_root(a):
    return a ** 0.5
# function to calculate the area of a circle and return the resul
def area_of_circle(r):
    return 3.14 * r * r
