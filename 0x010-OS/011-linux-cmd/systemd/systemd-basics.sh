# systemd is the init system for Linux systems. 
# It is a suite of system daemons that are started during the boot process and continue to run in the background.
# It is the default init system for most Linux distributions, including Fedora, Arch Linux, Debian, and Ubuntu.  

# systemd is a system and service manager for Linux operating systems.
# resposnsibility of the systemd is to start and stop services, and to manage the boot process.

