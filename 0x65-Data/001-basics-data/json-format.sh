# in json you can define 
# 1. key value pairs, 
# 2. arrays, []
# 3. objects and {} 
# 4. arrays of objects [{},{}]

# data types in json are - string, number, object, array, boolean, null 
# boolean example    "key" : true
# null example       "key" : null
# string example     "key" : "value"
# number example     "key" : 123
# array example      "key" : [ "value1" , "value2" , "value3" ]
# object example     "key" : { "key1" : "value1" , "key2" : "value2" }



# json define key value pair
# https://www.json.org/json-en.html 
 { "key" : "value" , "key2" : "value2"}
 # json example of key value pair
 { "name" : "John" , "age" : 30, "city" : "New York"}

# json define array
# https://www.json.org/json-en.html
 { "key" : [ "value1" , "value2" , "value3" ] }
 # json example of array
 { "name" : [ "John" , "Peter" , "Sally" ] }

# json define object
# https://www.json.org/json-en.html
 { "key" : { "key1" : "value1" , "key2" : "value2" } }
 # json example of object
 { "name" : { "first" : "John" , "last" : "Doe" } }

# json define array of objects
# https://www.json.org/json-en.html
 { "key" : [ { "key1" : "value1" , "key2" : "value2" } , { "key1" : "value1" , "key2" : "value2" } ] }
 # json example of array of objects
 { "name" : [ { "first" : "John" , "last" : "Doe" } , { "first" : "Peter" , "last" : "Pan" } ] }
